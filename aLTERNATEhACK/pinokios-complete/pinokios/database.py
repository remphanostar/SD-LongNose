def get_all_apps():
    return PINOKIO_APPS

def search_apps(query=""):
    results = []
    for app_id, app_data in PINOKIO_APPS.items():
        if not query or query.lower() in app_data.get('name', '').lower():
            results.append({'id': app_id, **app_data})
    return results

def get_stats():
    total = len(PINOKIO_APPS)
    clone_count = sum(1 for app in PINOKIO_APPS.values() if 'clone_url' in app)
    return {'total_apps': total, 'clone_url_count': clone_count}

